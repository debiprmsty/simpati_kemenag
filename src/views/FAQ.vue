<script setup>
import { ref, onMounted } from "vue";

const faqContainer = ref(null);

onMounted(() => {
  const detailsList = faqContainer.value.querySelectorAll("details");
  // 1) Pastikan semua tertutup saat mount
  detailsList.forEach((d) => (d.open = false));
  // 2) Saat satu dibuka, tutup sisanya
  detailsList.forEach((d) => {
    d.addEventListener("toggle", () => {
      if (d.open) {
        detailsList.forEach((other) => {
          if (other !== d) other.open = false;
        });
      }
    });
  });
});
</script>
